module.exports = {
	/**
	 * @example
	 * n1 = 1, n2 = 3 => result 4
	 * @param {*} n1 Number one of the sum
	 * @param {*} n2 Number two of the sum
	 */
	sum: (n1, n2) => n1 + n2,
	/**
	 * @example
	 * n1 = 1, n2 = 3 => result -2
	 * @param {*} n1 Number one of the substract
	 * @param {*} n2 Number two of the substract
	 */
	subtract: (n1, n2) => n1 - n2,
	/**
	 * @example
	 * n1 = 1, n2 = 3 => result 3
	 * @param {*} n1 Number one of the multiply
	 * @param {*} n2 Number two of the multiply
	 */
	multiply: (n1, n2) => n1 * n2,
	/**
	 * @example
	 * n1 = 1, n2 = 3 => result 0.333
	 * @param {*} n1 Number one of the divide
	 * @param {*} n2 Number two of the divide
	 */
	divide: (n1, n2) => n1 / n2,
}
